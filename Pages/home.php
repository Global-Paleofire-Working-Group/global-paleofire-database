		<p>The aim of the <b>Global Paleofire Database (GPD)</b> is to provide the scientific community with a global paleofire dataset for research and archiving sedimentary records of fire.</p>
		<p>The GPD (formerly GCD) has been founded by the GPWG and is now managed by the International Paleofire Network (IPN) in collaboration with the PAGES project and several partner universities over the world.</p>
		<p>The science emerging from the IPN is mainly:</p>
		<p>- the creation of a <b>public-access database</b> and an international research community with multiple-authored papers describing observed spatiotemporal changes in fire at global and regional scales (e.g. time series and maps).</p>
		<p>- <b>Global and regional syntheses</b> which enable the examination of broad-scale patterns in paleofire activity, creating a framework for exploring the <b>linkages among fire, Human, climate and vegetation</b> at centennial-to-multi-millennial time scales and allowing for <b>evaluation of fire model simulations</b> at regional to global scales.</p>
		<p> The computer infrastructure associated to the GPD is hosted at and managed by the staff of the Maison des Sciences de l’Homme et de l’Environnement (MSHE; USR CNRS-UFC) at the Université de Franche-Comté (Besançon, France), in collaboration with the staff of the Département de Géographie at the Université de Montréal.</p>
